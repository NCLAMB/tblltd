                        <span class="date">MAR 2009</span>
                        <h3>TBM Achieves SAFEcontractor Accreditation</h3>
                </header>
                        <p>TBM Ltd is one of the latest groups of successful companies to join a leading edge scheme, designed to help industry improve its safety record</p>
                        <p>The company's application for SAFEcontractor accreditation was driven by the need for a uniform standard across the business</p>
                        <p>SAFEcontractor accreditation is expected to enhance the company's ability to attract new contracts and its commitment to safety will be viewed positively by its insurers when the company liability policy is up for renewal.</p>
                        <p>SAFEcontractor is applicable to most sectors although it is particularly relevant to food manufacture, property, facilities management, retail and leisure sectors, all of which are big users of contract services.</p>
                        <p>Director's Chris Allman and Nick Hull explain that their success is due to the hard efforts of the TBM Team, both in the office and on site.</p>
                        <p>Darran Hughes; Operations Director of SAFEcontractor said</p>
                        <blockquote>Major organisations can no longer run the risk of employing contractors who are not able to prove that they have sound health and safety policies. More companies need to understand the importance of adopting good risk management in the way that TBM has done. The firms' high standard has set an example, which hopefully will be followed by other companies within the sector!</blockquote>
                        <p>Under the SAFEcontractor system, businesses undergo a vetting process, which examines health and safety procedures and their track record for safe practice. Those companies meeting the high standard are included on a database, which is accessible to registered users only via a website, <a href="http://www.safecontractor.com">www.safecontractor.com</a>.</p>
                        <p>Employer-organisations who sign up to the scheme can access the database, which enables them to vet potential contractors before they even set foot on site. These employers agree that, as users of the scheme, they will engage only those who have received accreditation.Over one hundred and twenty major nation-wide businesses, from several key sectors, have signed up to use the scheme when selecting contractors for services such as building, cleaning, maintenance, refurbishment or electrical and mechanical work.</p>
            
